# Ai Assignment 19 - Convert The Distance (In Feet) To Inches, Yards, And Miles
print("Convert The Distance (In Feet) To Inches, Yards, And Miles")
f = int(input("Enter Distance (In Feet) : "))
print("Distance in Inches :", f * 12)
print("Distance in Yards  :", f / 3)
print("Distance in Miles  :", f / 5280)
